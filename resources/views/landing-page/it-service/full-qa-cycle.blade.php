                              <p>System testing is conducted to explore system functionality. It is carried out during each build and during the release stage as alpha and beta testing of the product. For bigger projects it usually consists of creating use cases. System testing covers all aspects of a software system’s performance, its functionality and reliability. It involves testing for compliance with functional and non-functional requirements.</p>
                           </div>
